-- =====================================================
-- News（news_items）カテゴリを「両立型」に正規化
--   旧: 定款 / 事業報告 / お知らせ
--   新: お知らせ / プレス / イベント / 情報公開
--   - 「定款」「事業報告」は法定の情報公開なので「情報公開」へ統合
--   - 「お知らせ」はそのまま。プレス／イベントは今後の入稿で選択
-- 冪等：該当行が無ければ何もしない（既存データの安全な移行）。
-- カテゴリは text 列のためスキーマ変更は不要。
-- =====================================================

UPDATE news_items
  SET category = '情報公開'
  WHERE category IN ('定款', '事業報告');
